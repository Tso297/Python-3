def area():
    length = int(input('What is the length of the house?' ))
    width = int(input('What is the width of the house?' ))
    area = length * width
    print(area)
area()

def circumfrence():
    radius = int(input('What is the radius of the circle? '))
    circumfrence = 2 * 3.14 * radius
    print(circumfrence)
    
circumfrence()